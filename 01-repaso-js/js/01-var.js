// Variables - var - let - const
// Lenguaje de tipo dinámico, el tipo está dado por el valor de la variable y no por la variable en si, es decir no se define tipo de manera estatica

let cliente = 'Luciano'
let precio = 500

console.log(cliente)
console.log(precio)

// se pueden reasignar con otro tipo de dato
cliente = true
console.log(cliente)

// let puede iniciarse sin ningun valor - undefined
let esCliente
console.log(esCliente)

esCliente = true
console.log(esCliente)