const tech = ['HTML', 'CSS', 'JS', 'React', 'Node.js']

// foreach - ejecuta una vez por c/elemento, accede a c/elemento del array, solo list, no retorna el elemento
const arrForEach = tech.forEach(t => t)

// map - crea un nuevo arreglo
const arrMap = tech.map(t => t)

console.log(arrForEach)
console.log(arrMap)